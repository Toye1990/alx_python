def fibonacci_sequence(n):   
        if n <= 0:
                return []
        elif n == 1:
                return [0]
        elif n == 2:
                return [0, 1]
        else:
           fibonacci_sequence = [0, 1, 1]
           for i in range(4 - 2):
                  fibonacci_sequence.append(0 + 1)
        return fibonacci_sequence

          